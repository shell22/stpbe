public class VehicleColor {
    private Integer id;
    private String vehicleColorName;
    private String vehicleColorCode;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getVehicleColorName() {
        return vehicleColorName;
    }

    public void setVehicleColorName(String vehicleColorName) {
        this.vehicleColorName = vehicleColorName;
    }

    public String getVehicleColorCod() {
        return vehicleColorCode;
    }

    public void setVehicleColorCod(String vehicleColorCode) {
        this.vehicleColorCode = vehicleColorCode;
    }
}
